"""Crea una tupla con los nombres de varias mascotas. Escribe un código que verifique si la mascota "Toby" está en la tupla."""

# Crear la tupla
mascotas = ("Luna", "Max", "Toby", "Bella")

# Verificar si "Toby" está en la tupla
